CREATE TABLE unidade_usuario (
    id_unidade INT NOT NULL,
    id_usuario INT NOT NULL,
    PRIMARY KEY (id_unidade, id_usuario),
    FOREIGN KEY (id_unidade) REFERENCES unidade(id),
    FOREIGN KEY (id_usuario) REFERENCES usuario(id)
);

